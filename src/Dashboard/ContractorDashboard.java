    private void loadDashboardStats() {
        try {
            config cfg = new config();
            Connection conn = cfg.connectDB();
            
            PreparedStatement p1 = conn.prepareStatement("SELECT COUNT(*) as c FROM projects WHERE contractor_id = ?");
            p1.setInt(1, contractorId);
            ResultSet r1 = p1.executeQuery();
            if (r1.next()) lblTotalProjects.setText(String.valueOf(r1.getInt("c")));
            r1.close(); p1.close();
            
            PreparedStatement p2 = conn.prepareStatement("SELECT COUNT(*) as c FROM projects WHERE contractor_id = ? AND status = 'In Progress'");
            p2.setInt(1, contractorId);
            ResultSet r2 = p2.executeQuery();
            if (r2.next()) lblActiveProjects.setText(String.valueOf(r2.getInt("c")));
            r2.close(); p2.close();
            
            PreparedStatement p3 = conn.prepareStatement("SELECT SUM(pay.amount) as t FROM payments pay JOIN projects p ON pay.project_id = p.id WHERE p.contractor_id = ?");
            p3.setInt(1, contractorId);
            ResultSet r3 = p3.executeQuery();
            if (r3.next()) lblTotalEarnings.setText("‚Ç±" + String.format("%.2f", r3.getDouble("t")));
            r3.close(); p3.close();
            
            PreparedStatement p4 = conn.prepareStatement("SELECT SUM(p.budget - COALESCE(t.tot, 0)) as pend FROM projects p LEFT JOIN (SELECT project_id, SUM(amount) as tot FROM payments GROUP BY project_id) t ON p.id = t.project_id WHERE p.contractor_id = ? AND p.status = 'In Progress'");
            p4.setInt(1, contractorId);
            ResultSet r4 = p4.executeQuery();
            if (r4.next()) lblPendingPayments.setText("‚Ç±" + String.format("%.2f", r4.getDouble("pend")));
            r4.close(); p4.close();
            
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadAvailableProjects() {
         try {
            modelAvailableProjects.setRowCount(0);
            config cfg = new config();
            Connection conn = cfg.connectDB();
            
            String sql = "SELECT p.id, p.project_name, u.username as client, p.budget, p.status FROM projects p JOIN users u ON p.client_id = u.id WHERE p.status = 'Open' ORDER BY p.id DESC";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while (rs.next()) {
                modelAvailableProjects.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("project_name"),
                    rs.getString("client"),
                    "‚Ç±" + String.format("%.2f", rs.getDouble("budget")),
                    rs.getString("status")
                });
            }
            
            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

