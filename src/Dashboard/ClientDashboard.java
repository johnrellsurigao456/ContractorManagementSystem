    private void loadDashboardStats() {
         try {
            config cfg = new config();
            Connection conn = cfg.connectDB();
            
            PreparedStatement p1 = conn.prepareStatement("SELECT COUNT(*) as c FROM projects WHERE client_id = ?");
            p1.setInt(1, clientId);
            ResultSet r1 = p1.executeQuery();
            if (r1.next()) lblTotalProjects.setText(String.valueOf(r1.getInt("c")));
            r1.close(); p1.close();
            
            PreparedStatement p2 = conn.prepareStatement("SELECT COUNT(*) as c FROM projects WHERE client_id = ? AND status = 'In Progress'");
            p2.setInt(1, clientId);
            ResultSet r2 = p2.executeQuery();
            if (r2.next()) lblActiveProjects.setText(String.valueOf(r2.getInt("c")));
            r2.close(); p2.close();
            
            PreparedStatement p3 = conn.prepareStatement("SELECT SUM(amount) as t FROM payments WHERE project_id IN (SELECT id FROM projects WHERE client_id = ?)");
            p3.setInt(1, clientId);
            ResultSet r3 = p3.executeQuery();
            if (r3.next()) lblTotalSpent.setText("‚Ç±" + String.format("%.2f", r3.getDouble("t")));
            r3.close(); p3.close();
            
            PreparedStatement p4 = conn.prepareStatement("SELECT COUNT(*) as c FROM projects WHERE client_id = ? AND status = 'Completed'");
            p4.setInt(1, clientId);
            ResultSet r4 = p4.executeQuery();
            if (r4.next()) lblCompletedProjects.setText(String.valueOf(r4.getInt("c")));
            r4.close(); p4.close();
            
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    private void loadMyProjects() {
         try {
            modelProjects.setRowCount(0);
            config cfg = new config();
            Connection conn = cfg.connectDB();
            
            String sql = "SELECT p.id, p.project_name, COALESCE(u.username, 'Not Assigned') as contractor, p.budget, p.status FROM projects p LEFT JOIN users u ON p.contractor_id = u.id WHERE p.client_id = ? ORDER BY p.id DESC";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(1, clientId);
            ResultSet rs = pstmt.executeQuery();
            
            while (rs.next()) {
                modelProjects.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("project_name"),
                    rs.getString("contractor"),
                    "‚Ç±" + String.format("%.2f", rs.getDouble("budget")),
                    rs.getString("status")
                });
            }
            
            rs.close();
            pstmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    private void loadContractors() {
         try {
            modelContractors.setRowCount(0);
            config cfg = new config();
            Connection conn = cfg.connectDB();
            
            String sql = "SELECT id, username, status FROM users WHERE role = 'contractor' AND status = 'approved' ORDER BY id";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while (rs.next()) {
                modelContractors.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("username"),
                    rs.getString("status").toUpperCase()
                });
            }
            
            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void createProject() {
        JPanel panel = new JPanel(new GridLayout(3, 2, 10, 10));
        
        JTextField txtName = new JTextField();
        JTextField txtDesc = new JTextField();
        JTextField txtBudget = new JTextField();
        
        panel.add(new JLabel("Project Name:"));
        panel.add(txtName);
        panel.add(new JLabel("Description:"));
        panel.add(txtDesc);
        panel.add(new JLabel("Budget (‚Ç±):"));
        panel.add(txtBudget);
        
        int result = JOptionPane.showConfirmDialog(this, panel, "Create New Project", JOptionPane.OK_CANCEL_OPTION);
        
        if (result == JOptionPane.OK_OPTION) {
            String name = txtName.getText().trim();
            String desc = txtDesc.getText().trim();
            String budgetStr = txtBudget.getText().trim();
            
            if (name.isEmpty() || budgetStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fill all fields!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            try {
                double budget = Double.parseDouble(budgetStr);
                
                config cfg = new config();
                Connection conn = cfg.connectDB();
                
                String sql = "INSERT INTO projects (project_name, description, budget, client_id, status) VALUES (?, ?, ?, ?, 'Open')";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, name);
                pstmt.setString(2, desc);
                pstmt.setDouble(3, budget);
                pstmt.setInt(4, clientId);
                pstmt.executeUpdate();
                pstmt.close();
                conn.close();
                
                JOptionPane.showMessageDialog(this, "Project created!", "Success", JOptionPane.INFORMATION_MESSAGE);
                loadMyProjects();
                loadDashboardStats();
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid budget!", "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

