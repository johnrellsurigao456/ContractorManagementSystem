    private void makePayment() {
      int row = tblProjects.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a project first!", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    int projectId = (int) modelProjects.getValueAt(row, 0);
    String projectName = (String) modelProjects.getValueAt(row, 1);
    
    String amountStr = JOptionPane.showInputDialog(this, "Enter amount for: " + projectName, "Make Payment", JOptionPane.PLAIN_MESSAGE);
    
    if (amountStr != null && !amountStr.trim().isEmpty()) {
        try {
            double amount = Double.parseDouble(amountStr);
            
            config cfg = new config();
            Connection conn = cfg.connectDB();
            
            String sql = "INSERT INTO payments (project_id, amount, payment_date, status) VALUES (?, ?, date('now'), 'Paid')";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(1, projectId);
            pstmt.setDouble(2, amount);
            pstmt.executeUpdate();
            pstmt.close();
            conn.close();
            
            JOptionPane.showMessageDialog(this, "Payment of ‚Ç±" + String.format("%.2f", amount) + " recorded!", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadDashboardStats();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid amount!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}

